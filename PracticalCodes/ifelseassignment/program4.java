











class UpperLowerCase{
	public static void main(String[] args){

		char ch = 'a';
		char ch2 = 'A';

		if(ch == 'a'){
			System.out.println("a is a lowercase character");
		}else{
			System.out.println("A is a uppercase character");
		}

		if(ch2 == 'A'){
			System.out.println("A is a uppercase character");
		}else{
			System.out.println("a is a lowercase character");
		}
	}

}


