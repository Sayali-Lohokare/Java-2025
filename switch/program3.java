


class SwitchDemo2{
	public static void main(String[] args){

		char ch = 'E';

		switch(ch){
			default:
				System.out.println("Wrong");
			case 'A':
				System.out.println("A");
				break;
			case 'B':
				System.out.println("B");
				break;
			case 'C':
				System.out.println("C");
				break;
		}
	}


}


// if we r writing default at end of code then no need to write break but if not break is compulsary
